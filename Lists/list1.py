#List is a data type where we can store multiple items under 1 name. More technically, lists act like dynamic arrays which means you can add more items on the fly.
list  = [1,2,3,4] #Homogenous List
print(list)

list1 = [4,3,2,1]
print(list1 == list) # Both are not same because order of the list matters 

#2D list
print([1,2,3,[4,5,6]])

#3D list
print([[[1,2],[2,3]],[[5,6,7],[8,8]]])

#Heterogenous
print([1, True, 5.6,5+6,'Hello'])

